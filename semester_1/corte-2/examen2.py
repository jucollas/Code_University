def golposicion(l, g):
	i, ac, ans = 0, 0, 0 
	while i < len(l) and ac < g:
		ans = i
		ac += l[i]
		i += 1

	if ac < g:
		ans = -1

	return ans

def determinarEquipo(goles, eqs, g):
	p = golposicion(goles, g)
	if p == -1:
		ans = "Proximamente"
	else:
		ans = eqs[p]

	return ans

